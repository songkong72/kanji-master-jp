let fixes = 0;
data.forEach(d => {
    let changed = false;
    [d.on_sentence, d.kun_sentence].forEach(s => {
        if (!s || !s.text) return;
        const old = s.text;
        s.text = s.text.replace(/\[(.*?)\]\((.*?)\)/g, (match, kanjis, read) => {
            if (kanjis.length > 1 && kanjis.includes(d.kanji)) {
                // 1. Manual map
                if (manualSplits[kanjis]) {
                    // Check if reading length matches or it's a known irregular
                    const parts = manualSplits[kanjis];
                    const reconstructedReading = parts.map(p => p.match(/\((.*?)\)/)[1]).join('');
                    if (reconstructedReading === read) {
                        return parts.join('');
                    }
                }

                // 2. Equal length 1:1
                if (kanjis.length === read.length) {
                    let res = '';
                    for (let i = 0; i < kanjis.length; i++) res += `[${kanjis[i]}](${read[i]})`;
                    return res;
                }

                // 3. 2 kanjis, 4 reading (2-2)
                if (kanjis.length === 2 && read.length === 4) {
                    return `[${kanjis[0]}](${read.substring(0, 2)})[${kanjis[1]}](${read.substring(2)})`;
                }

                // 4. 2 kanjis, 3 reading (1-2 or 2-1)
                // If it's something like [意見](いけん), usually 1-2.
                // If the user's kanji is first, we prioritize that. 
                // But let's be safe.
                if (kanjis.length === 2 && read.length === 3) {
                    // Check if it's one of the known ones already handled.
                    // If not, we could guess, but better to add to map.
                }
            }
            return match;
        });
        if (s.text !== old) changed = true;
    });
    if (changed) fixes++;
});

